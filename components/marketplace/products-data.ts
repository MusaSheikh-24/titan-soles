export type Condition =
  | "NEW"
  | "USED"
  | "LIKE NEW"
  | "OPEN BOX"
  | "REFURBISHED";

export type MediaItem =
  | { type: "image"; src: string }
  | { type: "video"; src: string; poster: string };

export interface Product {
  id: number;
  name: string;
  brand: string;
  /** Verified marketplace seller / storefront */
  store: string;
  price: number;
  originalPrice: number;
  /** Primary image (always images[0]) */
  image: string;
  /** Gallery images — one product has multiple angles */
  images: string[];
  /** Optional product video (30s preview) */
  video?: string;
  category: string;
  gender: "Men" | "Women" | "Kids" | "Unisex";
  rating: number;
  reviews: number;
  colors: string[];
  sizes: number[];
  condition: Condition;
  verified: boolean;
  aiRecommended: boolean;
  trending?: boolean;
  onSale?: boolean;
}

